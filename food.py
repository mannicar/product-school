class Fruit(object):
	
	def __init__(self, name, calories, fiber):
		self.name = name
		self.calories = calories
		self.sweet = True
		self.fiber = fiber

class Meat:

	def __init__(self, name, calories, protein):

		self.name = name
		self.calories = calories
		self.sweet = False
		self.flavor = protein
	
